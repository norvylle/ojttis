create database companyrecords;
use companyrecords;

create table if not exists student(
	student_no varchar(10) not NULL,
	last_name varchar(50) default NULL,
	first_name varchar(50) default NULL,
	school varchar(50) default NULL,
	course varchar(50) default NULL,
	no_of_units int(3) default NULL,
	scholarship varchar(50) default NULL,
	constraint student_student_no_pk primary key(student_no)
);

create table if not exists company(
	company_name varchar(80) default NULL,
	address varchar(50) default NULL,
	constraint company_company_name_pk primary key(company_name)
);

create table if not exists intern(
	date_started date default NULL,
	no_of_hours_worked int(3) default NULL,
	no_of_hours_required int(3) default NULL,
	student_no varchar(10) not NULL,
	company_name varchar(80) not NULL,
	constraint intern_student_no_fk foreign key(student_no) references student(student_no) on update cascade on delete cascade,
	constraint intern_company_name_fk foreign key(company_name) references company(company_name) on update cascade on delete cascade
);

create table if not exists candidate(
	student_no varchar(10) not NULL,
	constraint candidate_student_no_fk foreign key(student_no) references student(student_no) on update cascade on delete cascade
);

create table if not exists audit_trail(
	id int(10) not null auto_increment,
	user_name varchar(50) default NULL,
	operation varchar(25) default NULL,
	table_name varchar(25) default NULL,
	column_name varchar(25) default NULL,
	old_value varchar(50) default NULL,
	new_value varchar(50) default NULL,
	trans_date date default NULL,
	constraint audit_trail_id_pk primary key(id)
);

create table if not exists constant(
	variable varchar(20) not null,
	value int(3) not null,
	description varchar(100) default null,
	constraint constant_variable_pk primary key(variable)
);

